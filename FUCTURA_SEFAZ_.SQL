CREATE TABLE TB_USERS (
CPF VARCHAR(20) NOT NULL,
EMAIL VARCHAR(20) NOT NULL,
PASSWORD VARCHAR(20) NOT NULL,
NAME VARCHAR(20),
PRIMARY KEY(CPF));

CREATE TABLE TB_VEHICLE (
BRAND VARCHAR(20),
MODELNAME VARCHAR(20) NOT NULL,
LICENSE VARCHAR(20) NOT NULL,
NICK_NAME VARCHAR(20),
CPF_USER VARCHAR(20) NOT NULL,
PRIMARY KEY (LICENSE));

CREATE TABLE TB_PART (
ID NUMBER NOT NULL,
NAME VARCHAR(20) NOT NULL,
PRICE FLOAT(10),
REPLACEDDATE DATE,
LICENSE_VEHICLE VARCHAR(20) NOT NULL,
PRIMARY KEY (ID));

ALTER TABLE TB_VEHICLE
ADD FOREIGN KEY (CPF_USER)
REFERENCES TB_USER(CPF);

ALTER TABLE TB_PART
ADD FOREIGN KEY (LICENSE_VEHICLE)
REFERENCES TB_VEHICLE (LICENSE);